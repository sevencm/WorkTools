package seven.tools.db;

public class CusDAOException extends Exception {

	private static final long serialVersionUID = 1L;

	public CusDAOException() {
		super();
	}
	
	public CusDAOException(String message, Throwable cause) {
		super(message, cause);
	}

	public CusDAOException(String message) {
		super(message);
	}

	public CusDAOException(Throwable cause) {
		super(cause);
	}

	
	
}
